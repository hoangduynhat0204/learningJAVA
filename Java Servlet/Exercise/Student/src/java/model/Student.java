package model;

/**
 *
 * @author Hoang Duy Nhat
 */
public class Student {
    private String id;
    private String name;
    private String email;
    private int classId;

    public Student() {
    }

    public Student(String id, String name, String email, int classId) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.classId = classId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getClassId() {
        return classId;
    }

    public void setClassId(int classId) {
        this.classId = classId;
    }
    
    
}
