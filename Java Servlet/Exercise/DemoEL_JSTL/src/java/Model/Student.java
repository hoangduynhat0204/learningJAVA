package Model;

/**
 *
 * @author Hoang Duy Nhat
 */
public class Student {
    private int studentId;
    private String name;
    private int mark;
    private String rank;

    public Student() {
    }

    public Student(int studentId, String name, int mark, String rank) {
        this.studentId = studentId;
        this.name = name;
        this.mark = mark;
        this.rank = rank;
    }

    public int getStudentId() {
        return studentId;
    }

    public void setStudentId(int studentId) {
        this.studentId = studentId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMark() {
        return mark;
    }

    public void setMark(int mark) {
        this.mark = mark;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }
    
    
}
